# ----------  Primitive Data Type  ---------- #

# string

# here [0] gives output only 'H' because H is at index 0 & it gives output value at index 0
print("Hello"[0])

# integer

print("123" + "456")
print(123 + 456)        # both are different

# float

print("123.456" + "123.456")
print(123.456 + 123.456)        # both are different

# Boolean

# it has only two possible values - "True" or "False" || "0" or "1" || "ON" or "OFF"

print("True or False")
print("0 or 1")
print("ON or OFF")
